#학생 5명의 국어,영어 점수
s1 <- c(70, 65)
s2 <- c(90, 95)
s3 <- c(65, 60)
s4 <- c(85, 90)
s5 <- c(60, 75)
s_all <- rbind(s1, s2, s3, s4, s5)
s_all
#######################################################
#맨해튼 거리
dist1 <- as.matrix(dist(s_all, method = "manhattan"))
dist1
#######################################################
#유클리드 거리
dist2 <- as.matrix(dist(s_all, method = "euclidean"))
dist2
#######################################################
