#코사인 거리
#같은 방향이면 거리0, 유사도1
#반대면 유사도 -1
#90도 방향이면 유사도 0
dist3 <- as.matrix(dist(s_all, method = "cosine"))
dist3
#######################################################
a <- c(1, 0, 5)
b <- c(4, 7, 3)
c <- c(40, 70, 30) #b의 10배(b와 c의 코사인 유사도는 1, 코사인 거리는 0)
items <- rbind(a,b,c) # 행 결합
colnames(items) <- c("v1", "v2", "v3")
items
#######################################################
#코사인 거리 계산(1 - 코사인유사도)
#두 벡터의 값이 같은 방향,같은 배수로 증가할 경우 코사인 거리는 0, 코사인 유사도는 1
dist4 <- as.matrix(dist(items, method = "cosine"))
dist4
